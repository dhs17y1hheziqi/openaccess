package com.versant.core.jdo.tools.workbench.ant;

/**
 * This class rep a ant target, with a name and description
 * @keep-all
 */
public class Target implements Comparable{
    private String name;
    private String description;

    public Target(String name, String description) {
        this.name = name;
        this.description = description;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public int compareTo(Object o) {
        return name.compareTo(((Target)o).getName());
    }
}

