package com.versant.core.jdo.junit.test3.model.externalizer;

import java.io.Serializable;

/**
 * For testing externalized types.
 */
public class SerializableType implements Serializable {

    private String data;

    public SerializableType() {
    }

    public SerializableType(String data) {
        this.data = data;
    }

    public String getData() {
        return data;
    }

    public void setData(String data) {
        this.data = data;
    }

    public String toString() {
        return data;
    }

}

