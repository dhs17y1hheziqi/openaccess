package com.versant.core.jdo.junit.test2.model;

/**
 * For testing null-if-not-found extension.
 * @keep-all
 */
public class NullIfNotFound {

    private String name;
    private Address address;    // null-if-not-found=true

    public NullIfNotFound(String name, Address address) {
        this.name = name;
        this.address = address;
    }

    public String getName() {
        return name;
    }

    public Address getAddress() {
        return address;
    }

}
