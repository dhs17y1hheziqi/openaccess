package com.versant.core.jdo.junit.test2.model;

/** 
 * For testing insert/update of a class with a LONGVARBINARY field (mapped to
 * a LONG RAW on Oracle).
 * @keep-all
 */
public class LongField {

    private String name;
    private byte[] data;

    public LongField(String name, byte[] data) {
        this.name = name;
        this.data = data;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public byte[] getData() {
        return data;
    }

    public void setData(byte[] data) {
        this.data = data;
    }

}
