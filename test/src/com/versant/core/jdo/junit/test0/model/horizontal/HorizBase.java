package com.versant.core.jdo.junit.test0.model.horizontal;

import com.versant.core.jdo.junit.test0.model.Address;

/**
 * For testing some horizontal inheritance stuff.
 */
public class HorizBase {

    private int age;
    private Address address;
    
    public HorizBase() {
    }

    public HorizBase(int age, Address address) {
        this.age = age;
        this.address = address;
    }

    public Address getAddress() {
        return address;
    }

    public void setAddress(Address address) {
        this.address = address;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }
    
}
