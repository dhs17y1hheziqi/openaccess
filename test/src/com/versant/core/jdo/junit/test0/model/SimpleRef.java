package com.versant.core.jdo.junit.test0.model;

/**
 * This has a name and a reference to Address. This is used to test navigating
 * references to non-existant rows.
 *
 * @keep-all
 */
public class SimpleRef {

    private String name;
    private Address address;    // must have jdbc-constraint=no

    public SimpleRef() {
    }

    public SimpleRef(String name, Address address) {
        this.name = name;
        this.address = address;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public Address getAddress() {
        return address;
    }

    public void setAddress(Address address) {
        this.address = address;
    }

}

 
